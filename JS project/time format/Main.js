let time =prompt("What time is it? (Enter 24 hour time format)");
    
if (time >= 1 && time < 12) {
    alert("Good morning")
} 

else if (time >= 12 && time < 17) {
    alert("Good afternoon")
} 
    
else if (time >= 17 && time < 20) {
    alert("Good evening")
} 
    
else if (time >= 20 && time < 25) {
    alert ("Good night")
} 
    
else {
    alert("Null")
}

    







